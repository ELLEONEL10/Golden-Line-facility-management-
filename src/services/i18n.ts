export function tr(lang: Lang, key: string): string {
  const val = T[lang]?.[key];
  if (typeof val === "string") return val;
  const fallback = T.de?.[key];
  if (typeof fallback === "string") return fallback;
  return key;
}

export function getFaqs(lang: Lang): FaqItem[] {
  const faqs = T[lang]?.["faqs"] ?? T.de["faqs"];
  if (Array.isArray(faqs)) {
    return faqs.map((f: unknown) => {
      if (typeof f === "object" && f !== null && "question" in f && "answer" in f) {
        return { question: String((f as Record<string, unknown>).question), answer: String((f as Record<string, unknown>).answer) };
      }
      return { question: "", answer: "" };
    });
  }
  return [];
}

